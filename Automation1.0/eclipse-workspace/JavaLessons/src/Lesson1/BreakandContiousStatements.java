package Lesson1;

public class BreakandContiousStatements {

	public static void main(String[] args) {
	
		// break statement:
		
			for(int i=1;i<=10;i++)
			   {
				if(i==5)	
			   {
				break;	
			    }	
				System.out.println(i);	
			    }
			
			System.out.println();
			
			
			
			//continuous statement:
			
			for(int i=1;i<=10;i++)
			   {
				if(i==5)	
			   {
			continue;	
			    }	
				System.out.println(i);	
			    }
			
			System.out.println();
			
		

	}

}
