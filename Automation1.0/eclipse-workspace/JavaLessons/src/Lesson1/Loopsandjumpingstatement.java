package Lesson1;

public class Loopsandjumpingstatement {
	
	public static void main(String[] args) {
		
		// While loops:
			
        int i=1;
		
		while(i<=10) {
		
		System.out.println(i);
		
		i++;
		
		}		
		System.out.println("program exit");
		
		System.out.println();
		
		
        int I=2;
		
		while(I<=10) {
		
		System.out.println(I);
		
		I+=2;
		
		}		
		System.out.println("program exit");
		
		System.out.println();
		
		
		// do while loop:
		
		int a=1;
		
		do 
		{
			
		System.out.println(a);
		a++;
		
		}while(a<=10);
		
		System.out.println();
		
        
		int A=1;
		
		do 
		{
			
		System.out.println(A);
		A+=2;
		
		}while(A<=10);
		
		System.out.println();
		
		// for loop:
		
		for(int b=1;b<=10;b++)
		{	
			System.out.println(b);
		}
		
		System.out.println("program exited");
		
		System.out.println();
		
		//decrease.
		
		for(int b=10;b>=1;b--)
		{	
			System.out.println(b);
		}
		
		System.out.println("program exited");
		
		System.out.println();
			
		
	}
	

}

